package C4_Java;

public class IncrementAndDecrement {
    public static void main(String[] args) {
        int b = 8;
        System.out.printf("%d%n", b);
        System.out.printf("%d%n", b++);
        System.out.printf("%d%n%n", b);

        b=8;
        System.out.printf("%d%n", b);
        System.out.printf("%d%n", ++b);
        System.out.printf("%d%n%n", b);

        b=8;
        System.out.printf("%d%n", b);
        System.out.printf("%d%n", b--);
        System.out.printf("%d%n%n",b);

        b=8;
        System.out.printf("%d%n", b);
        System.out.printf("%d%n", --b);
        System.out.printf("%d%n%n",b);


    }
}
