package C4_Java;

public class TabularOutput {
    public static void main(String[] args) {
        int i;
        int j;


        System.out.print("n     n1    n2    n3    n4");

        System.out.println();System.out.println();

        for(i=1;i<=5;i++){
            System.out.print(1+"     ");
        }

        System.out.println();

        for(j=1;j<=5;j++){
            System.out.print(2*j+"     ");
        }
        System.out.println();

        for(j=1;j<=5;j++){
            System.out.print(3*j+"     ");
        }
        System.out.println();

        for(j=1;j<=5;j++){
            System.out.print(4*j+"     ");
        }
        System.out.println();

        for(j=1;j<=5;j++){
            System.out.print(5*j+"     ");
        }
    }
}
